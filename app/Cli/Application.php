namespace App\Cli;

use inhere\console\App;
use inhere\library\di\Container;

/**
 * Class Application
 * @package App\Cli
 */
class Application extends App
{
    /**
     * @var Container
     */
    private $di;

    protected function init()
    {
        \Sws::$app = $this;

        parent::init(); // TODO: Change the autogenerated stub
    }

    /**
     * @param Container $di
     */
    public function setDi($di)
    {
        $this->di = $di;
    }

    /**
     * @return Container
     */
    public function getDi()
    {
        return $this->di;
    }
}